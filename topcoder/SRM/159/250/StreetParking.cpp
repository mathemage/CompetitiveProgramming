class StreetParking {
public:
	int freeParks(string);
};

int StreetParking::freeParks(string street) {
	int sz = street.size();
	vector<bool> free(sz);
	
	for (int i = 0; i < sz; i++) {
		switch (street[i]) {
		case 'D':
			free[i] = true;
			break;
		case 'B':
			free[i] = true;
			if (i >= 1) free[i-1] = true;
			if (i >= 2) free[i-2] = true;
			break;
		case 'S':
			free[i] = true;
			if (i >= 1) free[i-1] = true;
			if (i < sz - 1) free[i+1] = true;
			break;
		}
	}
	return sz - accumulate(free.begin(), free.end(), 0);
}


