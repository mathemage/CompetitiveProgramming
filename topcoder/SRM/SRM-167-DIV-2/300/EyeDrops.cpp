class EyeDrops {
public:
	double closest(int, int);
};

double EyeDrops::closest(int s, int k) {
	double t = 1440.0;
	t /= k;
	if (t >= 60*s) {
		return t;
	} else {
		return (1440.0-60*s)/(k-1);
	}
}


