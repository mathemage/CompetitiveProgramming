class Workshop {
public:
	int pictureFrames(vector <int>);
};

int Workshop::pictureFrames(vector <int> pieces) {
	int sz = pieces.size();
	int a, b, c, res = 0;
	for (int i = 0; i < sz; i++) {
		for (int j = i+1; j < sz; j++) {
			for (int k = j+1; k < sz; k++) {
				a = min(pieces[i], pieces[j]);
				a = min(a, pieces[k]);
				
				c = max(pieces[i], pieces[j]);
				c = max(c, pieces[k]);
				
				b = pieces[i]+pieces[j]+pieces[k]-a-c;
				res += a+b > c;
			}
		}
	}
	return res;
}


