class TypoCoderDiv2 {
public:
	int count(vector <int>);
};

int TypoCoderDiv2::count(vector <int> rating) {
	int count = 0;
	int cur = 0;
	
	for (int r : rating) {
		int next = (r >= 1200);
		count += abs(next - cur);
		cur = next;
	}
	return count;
}


