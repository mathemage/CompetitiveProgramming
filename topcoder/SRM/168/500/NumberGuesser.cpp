class NumberGuesser {
public:
	string digits(int n);
	int guess(string);
};

string NumberGuesser::digits(int n) {
	char c;
	string r;
	while (n) {
		c = '0' + n % 10;
		if (c != '0') r += c;
		n /= 10;
	}
	sort(r.begin(), r.end());
	return r;
}

int NumberGuesser::guess(string leftOver) {
	for (char d = '1'; d <= '9'; d++) {
		for (int pos = 0; pos < 4; pos++) {	
			string diff = leftOver;
  		
  		string dig(1, d);
			diff.insert(pos, dig);
			int c = stoi(diff);
			
			// test out: a - b = c
			int a;
			for (int b = 1; b < 9999; b++) {
				a = b + c;
				if (digits(a) == digits(b)) return d - '0';		 
			}
		}
	}
}


